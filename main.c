#include "philo.h"

int	main(int argc, char **argv)
{
	t_pro		process;

	if (argc < 2)
		return (1);
	get_args(&process, argv, argc);
	init_data_for_philos(&process);
	ft_init_mutex(&process);
	ft_create_tread(&process);
	ft_mutex_destroy(&process);
	free_all(&process);
	return (0);
}
